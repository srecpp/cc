def is_palindrome(s):
    
    s = s.lower().replace(" ", "")
    return s == s[::-1]

# Test cases
strings = ["radar", "hello", "A man a plan a canal Panama", "level"]

for string in strings:
    if is_palindrome(string):
        print(f"{string} is a palindrome")
    else:
        print(f"{string} is not a palindrome")
